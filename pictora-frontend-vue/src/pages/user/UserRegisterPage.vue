<template>
  <div id="userRegisterPage">
    <h2 class='title'>云图阁 - 用户注册</h2>
    <div class='desc'>企业级智能协同云图库</div>
    <a-form
      :model="formState"
      name="basic"
      @finish="handleSubmit"
    >
      <a-form-item
        name="userAccount"
        :rules="[{ required: true, message: '请输入账号' }, {min: 4, message: '账号不能小于 4 位'}]"
      >
        <a-input v-model:value="formState.userAccount" placeholder="请输入账号"/>
      </a-form-item>

      <a-form-item
        name="userPassword"
        :rules="[{ required: true, message: '请输入密码' }, {min: 8, message: '密码不能小于 8 位'}]"
      >
        <a-input-password v-model:value="formState.userPassword" placeholder="请输入密码"  />
      </a-form-item>
      <a-form-item
        name="checkPassword"
        :rules="[{ required: true, message: '请输入确认密码' }, {min: 8, message: '确认密码不能小于 8 位'}]"
      >
        <a-input-password v-model:value="formState.checkPassword" placeholder="请输入确认密码"  />
      </a-form-item>

      <a-form-item>
        <a-button type="primary" html-type="submit" style='width: 100%'>注册</a-button>
      </a-form-item>
    </a-form>
  </div>
</template>
<script lang="ts" setup>
import { reactive } from 'vue';
import { userLoginUsingPost, userRegisterUsingPost } from '@/api/userController.ts'
import { message } from 'ant-design-vue'
import router from '@/router'

// 用于接受表单输入的值
const formState = reactive<API.UserRegisterRequest>({
  userAccount: '',
  userPassword: '',
  checkPassword: ''
})

/**
 * 提交表单
 * @param values
 */
const handleSubmit = async (values: API.UserRegisterRequest) => {
  if (formState.userPassword !== formState.checkPassword) {
    message.error('两次密码不一致，请重试')
    return
  }
  const res = await userRegisterUsingPost(values)
  // 登录成功，把登录态保存到全局状态中
  if (res.data.code === 0 && res.data.data) {
    message.success('注册成功')
    router.push({
      path: '/user/login',
      replace: true
    })
  } else {
    message.error('注册失败，' + res.data.message)
  }
}

</script>

